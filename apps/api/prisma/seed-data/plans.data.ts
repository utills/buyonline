/** Map an age to the matching XL rate band key. */
export function getAgeBandKey(age: number): string {
  if (age <= 17)  return '5-17';
  if (age <= 25)  return '18-25';
  if (age <= 30)  return '26-30';
  if (age <= 35)  return '31-35';
  if (age <= 40)  return '36-40';
  if (age <= 45)  return '41-45';
  if (age <= 50)  return '46-50';
  if (age <= 55)  return '51-55';
  if (age <= 60)  return '56-60';
  if (age <= 65)  return '61-65';
  if (age <= 70)  return '66-70';
  if (age <= 75)  return '71-75';
  if (age <= 80)  return '76-80';
  return '81-85'; // 81-85 and 85+ share the same rates
}

/**
 * Look up the exact annual base premium from the XL rate card.
 * Falls back to null if the plan/age-band/SI combination is not in the map.
 */
export function lookupExactRate(planId: string, age: number, si: number): number | null {
  const band = getAgeBandKey(age);
  return EXACT_RATE_MAP[planId]?.[band]?.[si] ?? null;
}
